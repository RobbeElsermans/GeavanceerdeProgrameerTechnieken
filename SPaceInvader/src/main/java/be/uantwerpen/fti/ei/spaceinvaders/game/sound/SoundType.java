package be.uantwerpen.fti.ei.spaceinvaders.game.sound;

/**
 * De soorten geluiden die aanwezig zijn in game.
 */
public enum SoundType {
    /**
     * BONUS_SOUND bevat een geluid dat gebruikt wordt wanneer een bonus afgevuurd wordt.
     */
    BONUS_SOUND,
    /**
     * BIG_ENEMY_SOUND bevat een geluid dat gebruikt wordt wanneer een bigEnemy afgevuurd wordt.
     */
    BIG_ENEMY_SOUND,
    /**
     * ENEMY_DEAD_SOUND bevat een geluid dat gebruikt wordt wanneer een enemy dood gaat.
     */
    ENEMY_DEAD_SOUND,
    /**
     * PLAYER_SHOOT_SOUND bevat een geluid dat gebruikt wordt wanneer een player schiet.
     */
    PLAYER_SHOOT_SOUND,
    /**
     * PLAYER_DEAD_SOUND bevat een geluid dat gebruikt wordt wanneer een player dood gaat.
     */
    PLAYER_DEAD_SOUND,
    /**
     * BACKGROUND_MUSIC bevat een geluid dat als achtergrond muziek gebruikt kan worden.
     */
    BACKGROUND_MUSIC
}
